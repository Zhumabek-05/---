# 1. while циклы - пароль тексеру
print("while циклы мысалы:")
қателер = 0
while қателер < 3:
    пароль = input("Парольді енгізіңіз: ")
    if пароль == "12345":
        print("Қош келдіңіз!")
        break
    else:
        қателер += 1
        print(f"Қате пароль! {3-қателер} әрекет қалды")
else:
    print("Сіз блокталдыңыз!")
