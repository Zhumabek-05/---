# 2. Функция: тізімді элементтердің модулі бойынша кему ретімен сұрыптау
def list_sort(lst):
    return sorted(lst, key=lambda x: abs(x), reverse=True)

print("Модуль бойынша кему ретімен сұрыпталған:", list_sort(lst))

# 3. Функция: тізімнің бірінші және соңғы элементін орындарымен ауыстыру
def change(lst):
    if len(lst) >= 2:
        lst[0], lst[-1] = lst[-1], lst[0]
    return lst

